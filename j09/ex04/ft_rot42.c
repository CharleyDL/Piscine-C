char	*ft_rot42(char *str)
{
	int x;

	x = 0;
	while(str[x] != '\0')
	{
		if (str[x] >= 'a' && str[x] <= 'z')
			str[x] = (str[x] - 'a' + 16) % 26 + 'a';
		if (str[x] >= 'A' && str[x] <= 'Z')
			str[x] = (str[x] - 'A' + 16) % 26 + 'A';
		x++;
	}
	return (str);
}
