char	*ft_strupcase(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] >= 'a' && str[x] <= 'z')
		{
			str[x] -= 'a' - 'A';
			x++;
		}
		else if ((str[x] >= ' ' && str[x] <= '`') ||
					(str[x] >= '{' && str[x] <= '~'))
			x++;
	}
	return (str);
}
