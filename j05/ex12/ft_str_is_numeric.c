int		ft_str_is_numeric(char *str)
{
	int x;
	int res;

	x = 0;
	res = 0;
	if (str[0] == '\0')
		res = 1;
	else
	{
		while (str[x] != '\0')
		{
			if (str[x] >= '0' && str[x] <= '9')
				res = 1;
			else
				return (0);
			x++;
		}
	}
	return (res);
}
