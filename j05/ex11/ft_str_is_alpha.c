int		ft_str_is_alpha(char *str)
{
	int x;
	int res;

	x = 0;
	res = 0;
	if (str[0] == '\0')
		res = 1;
	else
	{
		while (str[x] != '\0')
		{
			if ((str[x] >= 'a' && str[x] <= 'z') ||
					(str[x] >= 'A' && str[x] <= 'Z'))
				res = 1;
			else
				return (0);
			x++;
		}
	}
	return (res);
}
