char	*ft_strlowcase(char *str)
{
	int x;

	x = 0;
	while (str[x] != '\0')
	{
		if (str[x] >= 'A' && str[x] <= 'Z')
		{
			str[x] -= 'A' - 'a';
			x++;
		}
		else if ((str[x] >= ' ' && str[x] <= '@') ||
				(str[x] >= '[' && str[x] <= '~'))
			x++;
	}
	return (str);
}
