int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int x;

	x = 0;
	while (x < n && x > 0)
	{
		if (s1[x] == s2[x])
			x++;
		else
			return (s1[x] - s2[x]);
	}
	return (0);
}
